#include "libft.h"

int	ft_toupper(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		if (c >= 'a' && c <= 'z')
		{
			c = c - 32;
		}
		return (c);
	}
	else
		return (c);
}
